	  function chkval()
	  {
		var msg="";
			var flag=true;
			  
			if(flag==false)
			{
			  alert (msg);
			  return false;
			}

			var name=document.forms["myfrm"]["quicname"].value;
			if(name==null || name=="")
			{
			   msg+="First Name must be filled out.\n";
			   flag = false;
			}
			var name=document.forms["myfrm"]["quiclname"].value;
			if(name==null || name=="")
			{
			   msg+="Last Name must be filled out.\n";
			   flag = false;
			}
			var x=document.forms["myfrm"]["quicemail"].value;
			var atpos=x.indexOf("@");
			var dotpos=x.lastIndexOf(".");
			if(x=="")
			{
			   msg+="E-mail address must be filled out.\n";
			   flag = false;
			}
			else
			{
			  if(atpos<1 || dotpos<atpos+2 || dotpos+2>=x.length)
			  {
			     msg+="Not a valid e-mail address.\n";
			 	 flag = false;
			  } 
			}
			var z=document.forms["myfrm"]["quicphone"].value;
		    if(z=="")
		    {
			  msg+="Contact Number must be filled out.\n";
			  flag = false;
		    }           
		    if(isNaN(z))
		    {
			  msg+="Please Enter Valid Contact Number.\n";
			  flag = false;
		    }           
			var scode=document.forms["myfrm"]["security_code"].value;
			if(scode==null || scode=="")
			{
			  msg+="Security code must be filled out.\n";
			  flag = false;
			}     
			if(flag==false)
			{
			  alert (msg);
		  	  return false;
			}
			else
			{
			  return true;
			}
		}
